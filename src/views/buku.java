    private void selectData() {
        String kolom[] = {"Id Buku", "Judul", "No ISBN","Penulis","Penerbit", "Tahun", "Stock", "Harga Pokok", "Harga Jual"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM buku";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
        while(rs.next()){
            String IdBuku       = rs.getString(1);
            String Judul        = rs.getString(2);
            String ISBN         = rs.getString(3);
            String Penulis      = rs.getString(4);
            String Penerbit     = rs.getString(5);
            String Tahun        = rs.getString(6);
            String Stock        = rs.getString(7);
            String HargaPokok   = rs.getString(8);
            String HargaJual    = rs.getString(9);
            String data[] = {IdBuku,Judul,ISBN,Penulis,Penerbit,Tahun,Stock,HargaPokok,HargaJual};
            dtm.addRow(data);
        }
        
        }catch(SQLException ex){
            Logger.getLogger(buku.class.getName()).log(Level.SEVERE, null, ex);
        }
        tabel.setModel(dtm);
    }
    
