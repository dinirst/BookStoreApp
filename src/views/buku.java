    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        if (idBuku.getText().equals("")|| judul.getText().equals("")|| isbn.getText().equals("")|| 
            penulis.getText().equals("")|| penerbit.getText().equals("")|| tahun.getText().equals("") || 
            stock.getText().equals("")|| pokok.getText().equals("")|| jual.getText().equals("")) {
           JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else{
            String SQL = "INSERT INTO buku VALUES" + "('"+idBuku.getText()+"','"+judul.getText()+"','"+isbn.getText()+"''"
                    +penulis.getText()+"','"+penerbit.getText()+"','"+tahun.getText()+"','"+stock.getText()+"','"+pokok.getText()
                    +"','"+jual.getText()+"')";
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan","Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else{
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_saveActionPerformed

